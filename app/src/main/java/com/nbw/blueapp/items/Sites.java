package com.nbw.blueapp.items;

public class Sites {

    private Long id;
    private String targetMain;
    private String targetDetail;
    private String local;
    private String income;
    private String age;
    private String gender;
    private String siteName;
    private String siteUrl;
    private String siteDetail;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTargetMain() {
        return targetMain;
    }

    public void setTargetMain(String targetMain) {
        this.targetMain = targetMain;
    }

    public String getTargetDetail() {
        return targetDetail;
    }

    public void setTargetDetail(String targetDetail) {
        this.targetDetail = targetDetail;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getIncome() {
        return income;
    }

    public void setIncome(String income) {
        this.income = income;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getSiteName() {
        return siteName;
    }

    public void setSiteName(String siteName) {
        this.siteName = siteName;
    }

    public String getSiteUrl() {
        return siteUrl;
    }

    public void setSiteUrl(String siteUrl) {
        this.siteUrl = siteUrl;
    }

    public String getSiteDetail() {
        return siteDetail;
    }

    public void setSiteDetail(String siteDetail) {
        this.siteDetail = siteDetail;
    }
}
